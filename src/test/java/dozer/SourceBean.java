package dozer;

import java.util.Date;

public class SourceBean {
	private Long id;

	private String name;
	
	private Date binary;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Date getBinary() {
		return binary;
	}

	public void setBinary(Date binary) {
		this.binary = binary;
	}


	
}
