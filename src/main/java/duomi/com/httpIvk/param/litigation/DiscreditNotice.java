package duomi.com.httpIvk.param.litigation;

public class DiscreditNotice {
	private String gender; // 性别
	private String implementationStatus; // 履行情况
	private String exeCid; // 执行依据文号
	private String name; // 被执行人姓名
	private String identificationNO; // 身份证号
	private String executableUnit; // 做出执行依据单位
	private String specificCircumstances; // 失信被执行人行为具体情形
	private String age; // 年龄
	private String postTime; // 发布时间
	private String caseNO; // 案号
	private String recordTime; // 立案时间
	private String court; // 执行法院名称
	private String type; // 标识自然人或企业
	private String province; // 省份

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getImplementationStatus() {
		return implementationStatus;
	}

	public void setImplementationStatus(String implementationStatus) {
		this.implementationStatus = implementationStatus;
	}

	public String getExeCid() {
		return exeCid;
	}

	public void setExeCid(String exeCid) {
		this.exeCid = exeCid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIdentificationNO() {
		return identificationNO;
	}

	public void setIdentificationNO(String identificationNO) {
		this.identificationNO = identificationNO;
	}

	public String getExecutableUnit() {
		return executableUnit;
	}

	public void setExecutableUnit(String executableUnit) {
		this.executableUnit = executableUnit;
	}

	public String getSpecificCircumstances() {
		return specificCircumstances;
	}

	public void setSpecificCircumstances(String specificCircumstances) {
		this.specificCircumstances = specificCircumstances;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}

	public String getPostTime() {
		return postTime;
	}

	public void setPostTime(String postTime) {
		this.postTime = postTime;
	}

	public String getCaseNO() {
		return caseNO;
	}

	public void setCaseNO(String caseNO) {
		this.caseNO = caseNO;
	}

	public String getRecordTime() {
		return recordTime;
	}

	public void setRecordTime(String recordTime) {
		this.recordTime = recordTime;
	}

	public String getCourt() {
		return court;
	}

	public void setCourt(String court) {
		this.court = court;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

}
