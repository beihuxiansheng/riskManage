package duomi.com.httpIvk.param.multipleLend;

public class loanLenderDetails {
	//平台类型
	public String type;
	//平台代码
	public String code;
	//放款时间
	public String time;
	//放款金额
	public String loanlendersAmount;
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getLoanlendersAmount() {
		return loanlendersAmount;
	}
	public void setLoanlendersAmount(String loanlendersAmount) {
		this.loanlendersAmount = loanlendersAmount;
	}
	
	
}
