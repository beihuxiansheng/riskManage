public class CreditPlatformRegistrationDetails {
	//平台类型
	public String type;
	//平台代码
	public String code;
	//注册时间
	public String time;
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	
}
