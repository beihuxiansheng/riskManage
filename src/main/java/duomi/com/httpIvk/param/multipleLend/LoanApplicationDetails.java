package duomi.com.httpIvk.param.multipleLend;

public class LoanApplicationDetails {
	//平台类型
	public String type;
	//平台代码
	public String code;
	//申请时间
	public String time;
	//申请金额
	public String applicationAmountut;
	//申请结果
	public String applicationResult;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getApplicationAmountut() {
		return applicationAmountut;
	}
	public void setApplicationAmountut(String applicationAmountut) {
		this.applicationAmountut = applicationAmountut;
	}
	public String getApplicationResult() {
		return applicationResult;
	}
	public void setApplicationResult(String applicationResult) {
		this.applicationResult = applicationResult;
	}
	

}
